DROP TABLE IF EXISTS PUBLIC.PERSON;
DROP TABLE IF EXISTS PUBLIC.PHONE;
DROP TABLE IF EXISTS PUBLIC.PHONE_DETAILS;
DROP TABLE IF EXISTS PUBLIC.PASSPORT;
DROP TABLE IF EXISTS PUBLIC.ADDRESS;
DROP TABLE IF EXISTS PUBLIC.PERSON_ADDRESS;

CREATE SEQUENCE IF NOT EXISTS PUBLIC.PERSON_SEQ;
CREATE SEQUENCE IF NOT EXISTS PUBLIC.PHONE_SEQ;
CREATE SEQUENCE IF NOT EXISTS PUBLIC.PHONE_DETAILS_SEQ;
CREATE SEQUENCE IF NOT EXISTS PUBLIC.PASSPORT_DETAILS_SEQ;
CREATE SEQUENCE IF NOT EXISTS PUBLIC.ADDRESS_SEQ;

CREATE TABLE PUBLIC.PERSON(
   PERSON_ID BIGINT DEFAULT PERSON_SEQ.nextval,
   PERSON_FIRST_NAME VARCHAR(50) NOT NULL,
   PERSON_MIDDLE_NAME VARCHAR(50) NOT NULL,
   PERSON_LAST_NAME VARCHAR(50) NOT NULL,
   PERSON_EMAIL VARCHAR(50) NOT NULL,
   PRIMARY KEY (PERSON_ID)
);

CREATE TABLE PUBLIC.PHONE(
    PHONE_ID BIGINT DEFAULT PHONE_SEQ.nextval,
    PERSON_ID BIGINT,
    PHONE_NUMBER VARCHAR(50),
    DETAILS_ID BIGINT,
    PRIMARY KEY (PHONE_ID),
    FOREIGN KEY (PERSON_ID) REFERENCES PUBLIC.PERSON
);

CREATE TABLE PUBLIC.PHONE_DETAILS(
    PHONE_DETAILS_ID BIGINT DEFAULT PHONE_DETAILS_SEQ.nextval,
    MANUFACTURER VARCHAR(50),
    MODEL VARCHAR(50),
    COLOR VARCHAR(50),
    PHONE_TYPE VARCHAR (50),
    PHONE_ID Integer NOT NULL,
    PRIMARY KEY (PHONE_DETAILS_ID),
    FOREIGN KEY (PHONE_ID) REFERENCES PUBLIC.PHONE
);

CREATE TABLE PUBLIC.PASSPORT(
    DOC_ID BIGINT DEFAULT PASSPORT_DETAILS_SEQ.nextval,
    DOC_SERIAL_NUMBER VARCHAR(30) NOT NULL,
    DOC_ISSUE_DATE DATE,
    DOC_EXPIRATION_DATE DATE,
    DOC_ISSUER_COUNTRY VARCHAR(25) NOT NULL,
    PERSON_ID Integer NOT NULL,
    PRIMARY KEY (DOC_ID),
    FOREIGN KEY (PERSON_ID) REFERENCES PUBLIC.PERSON
);


CREATE TABLE PUBLIC.ADDRESS (
    ADDR_ID BIGINT DEFAULT ADDRESS_SEQ.nextval,
    ADDR_NUMBER VARCHAR(20),
    ADDR_STREET VARCHAR(100),
    ADDR_ZIPCODE VARCHAR (30),
    ADDR_CITY VARCHAR (100),
    PERSON_ID BIGINT,
    PRIMARY KEY (ADDR_ID)
);


CREATE TABLE PERSON_ADDRESS (
    PERSON_ID BIGINT NOT NULL,
    ADDR_ID BIGINT NOT NULL,
    FOREIGN KEY (PERSON_ID) REFERENCES PUBLIC.PERSON,
    FOREIGN KEY (ADDR_ID) REFERENCES PUBLIC.ADDRESS
)






